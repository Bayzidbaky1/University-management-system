                The main article is here 11 teachers, students of UODA get awards from Japan exhibition
                </p>
                <p>
                 11 teachers and students University of Development Alternative (UODA) have received awards from 11th Kyokubi Tsukuba Exhibition in Japan recently.
                </p>
                <p>
                 A press release said, twenty one students and four teachers of Fine Arts Department of UODA joined the exhibition.
                </p>
                <p>
                  Among them, 72-year-old teacher Professor Alauddin Ahmed got the grand award and Assistant Professor Mahabub Alam won semi-grand award.
                </p>
                <p>
                  Associate Professor Kaniz Sohanee Islam and student Sajal Sutradhar received Fujisan Art Special award.

Meanwhile, Associate Professor Uttam Kumar Saha Chowdhury got Fujisan Art Excellence award along with six students; Ritu Joana Gomez, Tamanna Binte Sorwar, Sumon Biswas, Tawfiq Bin Sakil, Sharmin Jahan Khan and Jayanata Sarker John.
                </p>
                <p>
                  The university arranged a ceremony to hand over the awards at its auditorium in Dhanmondi area of Dhaka on Thursday
                </p>
                <p>
                  UODA founder and president Professor Mujib Khan was present at the ceremony as chief guest. Vice-President of UODA trustee board Dalia Nahid Khan, Vice-Chancellor Professor Dr Rafiqul Islam Sharif were also present as special guest.
                </p>
                <p>
                  Treasurer, registrar, deans, head of the departments and other teachers were also present at the function. Gloria Foundation of Japan's Munshiganj unit president Md Tarique also joined the ceremony.

Chairman of UODA's fine arts department and eminent artist Shahjahan Ahmed Bikash presided over the programme, which was followed by a lunch.
                </p>



                
